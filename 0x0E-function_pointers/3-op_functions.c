/**
 * op_add - Adds
 * @a: first int coming in
 * @b: second int coming in
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Subtracts
 * @a: first int coming in
 * @b: second int coming in
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiplies
 * @a: first int coming in
 * @b: second int coming in
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides
 * @a: first int coming in
 * @b: second int coming in
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Modulus
 * @a: first int coming in
 * @b: second int coming in
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
